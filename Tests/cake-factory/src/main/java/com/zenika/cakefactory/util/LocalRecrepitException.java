package com.zenika.cakefactory.util;

/**
 * @author gtinon
 */
public class LocalRecrepitException extends RuntimeException {

	public LocalRecrepitException() {
	}

	public LocalRecrepitException(String message) {
		super(message);
	}

	public LocalRecrepitException(String message, Throwable cause) {
		super(message, cause);
	}

	public LocalRecrepitException(Throwable cause) {
		super(cause);
	}

}
